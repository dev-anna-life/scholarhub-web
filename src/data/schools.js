export const nigerianStates = ['abia','adamawa','akwa ibom','anambra','bauchi','bayelsa','benue','borno','cross river','delta','ebonyi','edo','ekiti','enugu','gombe','imo','jigawa','kaduna','kano','katsina','kebbi','kogi','kwara','lagos','nasarawa','niger','ogun','ondo','osun','oyo','plateau','rivers','sokoto','taraba','yobe','zamfara','fct','abuja']

export function getCountryFromState(state) {
  if (!state) return null
  const s = state.toLowerCase().trim()
  if (nigerianStates.some(st => s.includes(st))) return 'Nigeria'
  if (s.includes('nairobi') || s.includes('mombasa') || s.includes('kisumu') || s.includes('eldoret') || s.includes('kenya')) return 'Kenya'
  if (s.includes('accra') || s.includes('kumasi') || s.includes('cape coast') || s.includes('ghana')) return 'Ghana'
  if (s.includes('johannesburg') || s.includes('cape town') || s.includes('durban') || s.includes('pretoria') || s.includes('stellenbosch') || s.includes('south africa')) return 'South Africa'
  if (s.includes('kampala') || s.includes('entebbe') || s.includes('uganda')) return 'Uganda'
  if (s.includes('addis') || s.includes('ethiopia')) return 'Ethiopia'
  if (s.includes('cairo') || s.includes('alexandria') || s.includes('giza') || s.includes('egypt')) return 'Egypt'
  if (s.includes('dar es salaam') || s.includes('dodoma') || s.includes('tanzania')) return 'Tanzania'
  if (s.includes('khartoum') || s.includes('sudan')) return 'Sudan'
  if (s.includes('gaborone') || s.includes('botswana')) return 'Botswana'
  if (s.includes('dakar') || s.includes('senegal')) return 'Senegal'
  if (s.includes('abidjan') || s.includes("côte") || s.includes('ivoire') || s.includes('ivory')) return "Côte d'Ivoire"
  if (s.includes('nigeria')) return 'Nigeria'
  return null
}

export function getSchoolsForUser(userState, level) {
  const country = getCountryFromState(userState)
  if (country && schoolsByCountry[country] && schoolsByCountry[country][level]?.length > 0) {
    return { schools: schoolsByCountry[country][level], country }
  }
  return { schools: featuredSchools[level], country: null }
}

export function getSchoolLogo(name) {
  const slug = name.toLowerCase()
    .replace(/[^a-z0-9\s-]/g, '')
    .replace(/\s+/g, '-')
    .replace(/-+/g, '-')
    .replace(/^-|-$/g, '')
  const png = `/images/schools/${slug}.png`
  const svg = `/images/schools/${slug}.svg`
  return { png, svg, slug }
}

export function getAllSchoolsForLevel(level) {
  const all = []
  for (const [country, data] of Object.entries(schoolsByCountry)) {
    if (data[level]?.length) {
      all.push(...data[level].map(s => ({ ...s, country })))
    }
  }
  return all
}
